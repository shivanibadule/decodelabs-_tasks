import pandas as pd

# load the raw data
df = pd.read_excel("/content/Dataset for Data Analytics.xlsx")

print("Rows before cleaning:", len(df))
print("Missing values per column:")
print(df.isnull().sum())

# remove duplicate rows
df = df.drop_duplicates()
df = df.drop_duplicates(subset="OrderID")

# CouponCode is blank when no coupon was used, not a real missing value
df["CouponCode"] = df["CouponCode"].fillna("No Coupon")

# clean up text columns - remove extra spaces and fix casing
text_cols = ["Product", "PaymentMethod", "OrderStatus", "ReferralSource"]
for col in text_cols:
    df[col] = df[col].str.strip().str.title()

df["OrderID"] = df["OrderID"].str.strip().str.upper()
df["TrackingNumber"] = df["TrackingNumber"].str.strip().str.upper()
df["CustomerID"] = df["CustomerID"].str.strip()

# make sure dates are proper dates
df["Date"] = pd.to_datetime(df["Date"], errors="coerce")
df = df.dropna(subset=["Date"])

# round money columns to 2 decimals
df["UnitPrice"] = df["UnitPrice"].round(2)
df["TotalPrice"] = df["TotalPrice"].round(2)

# drop any rows with bad quantity or price values
df = df[(df["Quantity"] > 0) & (df["UnitPrice"] > 0)]

# check that TotalPrice actually matches Quantity x UnitPrice, fix if not
correct_total = (df["Quantity"] * df["UnitPrice"]).round(2)
df["TotalPrice"] = correct_total

print("\nRows after cleaning:", len(df))
print("Missing values after cleaning:")
print(df.isnull().sum())

df.to_excel("Cleaned_Dataset.xlsx", index=False)
print("\nSaved cleaned file as Cleaned_Dataset.xlsx")